            <div class="card-body">
                <h5 class="card-title">3. London System</h5>
                <p class="card-text">Starts with<strong> 1.d4 and 2.Bf4 or 2.Nf3 followed by Bf4 </strong>. A solid and easy-to-learn system for White, popular at all levels of play.</p>
                <p><strong>Key Ideas:</strong> Early development of bishop outside the pawn chain, safe king, flexible setup.</p>
                <p><strong>Strengths:</strong> Hard to refute, good for beginners and masters alike.</p>
                <p><strong>Used By:</strong> Magnus Carlsen, Levon Aronian</p>
            </div>
        </div>
    </div>
</div>

