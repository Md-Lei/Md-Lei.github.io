                <h5 class="card-title">4. Bishop's Opening</h5>
                <p class="card-text">A classical opening that begins with <strong>1.e4 e5 2.Bc4</strong>, focusing on rapid development and control of the center with the bishop.</p>
                <p><strong>Key Ideas:</strong> Develop the bishop early, control the center, prepare for a kingside attack.</p>
                <p><strong>Strengths:</strong> Fast development, pressure on f7, flexible and often leads to open positions.</p>
                <p><strong>Used By:</strong> Many strong players including Bobby Fischer and Garry Kasparov.</p>
            </div>
        </div>
    </div>
</div>


