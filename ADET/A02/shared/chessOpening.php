                <h5 class="card-title">2. Queen's Gambit</h5>
                <p class="card-text">Starts with,<strong> 1.d4 d5 2.c4. </strong>A strong positional opening where White offers a pawn to gain better central control and open lines for development.</p>
                <p><strong>Key Ideas:</strong> Create tension in the center, control the d5-square, maintain structural balance.</p>
                <p><strong>Famous Trap:</strong> Elephant Trap (if mishandled by White)</p>
                <p><strong>Used By:</strong> Beth Harmon (fictional), Vladimir Kramnik</p>
